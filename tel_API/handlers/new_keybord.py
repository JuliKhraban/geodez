from telebot.types import ReplyKeyboardMarkup, KeyboardButton
from tel_API.keyboard.lexicon import LEXICON_RU, LEXICON_RU1

def search_you_chast_top1(data):
    for key in LEXICON_RU:
        if LEXICON_RU[key] == data:
            LEXICON_RU1[key] = '✅' + LEXICON_RU[key]
    keyboard = ReplyKeyboardMarkup(resize_keyboard=True, row_width=2)
    keyboard1 = KeyboardButton(LEXICON_RU1['fio'])
    keyboard2 = KeyboardButton(LEXICON_RU1['num_pasport'])
    keyboard3 = KeyboardButton(LEXICON_RU1['data_pasport'])
    keyboard4 = KeyboardButton(LEXICON_RU1['give_pasport'])
    keyboard5 = KeyboardButton(LEXICON_RU1['adress_pr'])
    keyboard6 = KeyboardButton(LEXICON_RU1['number_kadastr'])
    keyboard7 = KeyboardButton(LEXICON_RU1['telephon'])
    keyboard8 = KeyboardButton(LEXICON_RU1['next'])
    keyboard.add(keyboard1, keyboard2, keyboard3, keyboard4, keyboard5, keyboard6, keyboard7, keyboard8)
    return keyboard

